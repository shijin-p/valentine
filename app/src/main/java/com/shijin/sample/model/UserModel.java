package com.shijin.sample.model;

public class UserModel {

    private String id;
    private String name;
    private String username;
    private ProfileImageModel profile_image;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getUsername() {
        return username;
    }

    public ProfileImageModel getProfileImage() {
        return profile_image;
    }
}
